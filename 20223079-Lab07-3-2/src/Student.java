public class Student {
	private String names;
	private int scores;
		
	public String getName() {
		return names;
	}
	
	public void setName(String names) {
		this.names = names;
	}
	
	public int getScore() {
		return scores;
	}
	
	public void setScore(int scores) {
		this.scores = scores;
	}
	
}
